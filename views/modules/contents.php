		   	<p>
		   		El braille (pronunciado /bráille/,1​ /bráiye/1​ o /bráile/) es un sistema de lectura y escritura táctil pensado para personas ciegas. Se conoce también como cecografía. 
		   		<br>
		   		Fue ideado a mediados del siglo XIX por el francés Louis Braille (1809-1852), que se quedó ciego debido a un accidente durante su niñez mientras jugaba en el taller de su padre.
		   		<br>
		   		Cuando tenía 13 años, el director de la Escuela de Ciegos y Sordos de París –donde estudiaba el joven Braille– le pidió que probara un sistema de lectoescritura táctil inventado por un militar llamado Charles Barbier de la Serre para transmitir órdenes a puestos de avanzada sin tener necesidad de delatar la posición durante las noches. 
		   		<br>
		   		Louis Braille descubrió al cabo de un tiempo que el sistema era válido y lo reinventó utilizando un sistema de 8 puntos. Al cabo de unos años lo simplificó dejándolo en el sistema universalmente conocido y adoptado de 6 puntos. 
		   	</p>
